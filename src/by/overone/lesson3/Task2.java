package by.overone.lesson3;

public class Task2 {
    public static void main(String[] args) {
        int num1 = 125;
        int num2 = 24;
        System.out.println(num1 + " + " + num2 + " = " + (num1 + num2));
        System.out.println(num1 + " - " + num2 + " = " + (num1 - num2));
        System.out.println(num1 + " * " + num2 + " = " + (num1 * num2));
        System.out.println(num1 + " / " + num2 + " = " + (num1 / num2));
        System.out.print(num1 + " % " + num2 + " = " + (num1 % num2));
    }
}
